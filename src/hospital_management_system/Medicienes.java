    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            String name = jTextField1.getText();
            String company = jTextField4.getText();
            String expiry_date = jTextField2.getText();
            String cost = jTextField3.getText();

            FileReader fr = new FileReader("medicienes.txt");
            BufferedReader br = new BufferedReader(fr);

            String currentLine;

            List<String> lines = new ArrayList<>();
            while ((currentLine = br.readLine()) != null) {
                String[] LaboratoryDetail = currentLine.split(",");
                if (!LaboratoryDetail[0].equals(name)) {
                    lines.add(currentLine);
                }
            }
            br.close();
            fr.close();

            FileWriter fw = new FileWriter("medicienes.txt");
            BufferedWriter bw = new BufferedWriter(fw);

            for (String line : lines) {
                bw.write(line);
                bw.newLine();
            }

            bw.close();
            fw.close();
            display2();

            System.out.println("\n medicienes record deleted successfully!");

            display2();
        } catch (IOException ex) {
            Logger.getLogger(Laboratory.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed


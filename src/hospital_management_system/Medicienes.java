    void display2() throws FileNotFoundException, IOException {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);
        FileReader fr = new FileReader("medicienes.txt");
        BufferedReader br = new BufferedReader(fr);

        String currentLine;

        System.out.println("\nList of all madicienes:");
        while ((currentLine = br.readLine()) != null) {
            String[] medicienesDetail = currentLine.split(",");
            System.out.println("Name: " + medicienesDetail[0] + ", Company: " + medicienesDetail[1] + ", Expiry Date: " + medicienesDetail[2] + ", Cost: " + medicienesDetail[3]);
            Object obj[] = {medicienesDetail[0], medicienesDetail[1], medicienesDetail[2], medicienesDetail[3]};
            model.addRow(obj);
        }
        br.close();
        fr.close();
    }


    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String name = jTextField1.getText();
        String company = jTextField4.getText();
        String expiry_date = jTextField2.getText();
        String cost = jTextField3.getText();

        FileReader fr = null;
        try {
            // TODO add your handling code here:
            fr = new FileReader("medicienes.txt");
            BufferedReader br = new BufferedReader(fr);
            String currentLine;
            List<String> lines = new ArrayList<>();
            while ((currentLine = br.readLine()) != null) {
                String[] laboratoryDetail = currentLine.split(",");
                if (laboratoryDetail[0].equals(name)) {
                    lines.add(name + "," + company + "," + expiry_date + "," + cost);

                } else {
                    lines.add(currentLine);
                }
            }
            br.close();
            fr.close();
            FileWriter fw = new FileWriter("medicienes.txt");
            BufferedWriter bw = new BufferedWriter(fw);
            for (String line : lines) {
                bw.write(line);
                bw.newLine();
            }
            bw.close();
            fw.close();
            display2();

            System.out.println("\nmedicienes record updated successfully!");

        } catch (IOException ex) {
            Logger.getLogger(Medicienes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

